            <br>
            <p>相比小學堂，此處各地字表、韻書數據均是由嶺南粵音群內各地學<span class="del">姐</span>者提供（詳見本文底部名單），由此地方音基本爲他等自我審查並去除猩化後的當地音。可能有少數字會被手動高舉化，但總體上還是能反映當地本來讀音情況的。</p>
