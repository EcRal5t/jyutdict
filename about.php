            <h3>㈤ 問與答</h3>
            <p>Ⅰ. 爲何地方音要放擴展粵拼/這個擴展粵拼有何意義而不直接用IPA</p>
            <p>ⅰ. 實際上，這裏一共有三處使用了粵拼：1.地方音展示 2.按音查字 3.泛粵表，如果僅使用IPA，先不說對大眾的門檻會升高多少，還會使各處的錄入工作變得相對麻煩，再者泛粵粵語音系相對統一，常用的擴展粵拼相對原來粵拼並未有大幅度變化，學習成本並不高；其次，地方音展示處並非完全不使用IPA，但其用在了當地偏向嚴式的表記上，一方面可更準確記錄當地音，然而也會削弱了不同地方讀音之間的聯繫，因而再補上了粵拼，這樣也可以附帶調號以表示調類情況。</p>
            <br>
            <p>（注意：不是所有地方都有偏嚴式的IPA，而且即使使用了寬式IPA也不代表就是足夠合理的，這受限於是否能有條件去分析當地實際讀音，以及字表提供者本身的口音偏差，這也是不能只使用IPA的原因之一。）</p>
            <br>
            <p>Ⅱ. 功能真少/好簡陋</p>
