            <h1>一、泛粵大典</h1>
            <p>目前，泛粵典旨在收集現時各地讀音與歷史韻書讀音並提供一些基礎的查詢功能，包括查字、查音，未來再繼續加入查韻（按音韻地位檢索）及查詞等功能和更多的字表。</p>
