                <h4>聲母：</h4>
                <p>z c s / zh ch sh：分韻精照（精i相容師韻ii，故毋再標）</p>
                <p>j/nj：分韻日母</p>
                <h4>韻母：</h4>
                <p>aa/ae/e/i：ae儘量表示二等，視乎順德等埞有冇高化；e陰聲韻儘量表示蟹同埋支白，i/e陽(入)聲韻見後</p>
                <p>o/oe：oe除唨音系表示果三合之外，亦表示果開白讀，視乎具體文白</p>
                <p>ui/ooi/yi/yu：分韻魁(ui)雖(ooi)諸(yu)韻，切要裂化精組遇(yi)視乎各埞有冇裂化</p>
                <p>im in ing / em en eng：三四等文白，儘量照顧白讀，亦考慮各方音文白比例（入聲韻同理）</p>
                <p>ing / yng：梗曾三四等，視乎鬱林等埞有冇圓脣（入聲韻同理）</p>
